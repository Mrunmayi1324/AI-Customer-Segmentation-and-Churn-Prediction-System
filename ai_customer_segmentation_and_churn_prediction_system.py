import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

df = pd.read_csv("/content/transaction_data.csv")
df['Transaction_Date'] = pd.to_datetime(df['Transaction_Date'])

df.head()

"""# Create RFM Features"""

recency = (
    df['Transaction_Date'].max() -
    df.groupby('Customer_ID')['Transaction_Date'].max()
).dt.days

frequency = df.groupby('Customer_ID')['Transaction_ID'].count()
monetary = df.groupby('Customer_ID')['Transaction_Amount'].sum()

rfm = pd.DataFrame({
    'Recency': recency,
    'Frequency': frequency,
    'Monetary': monetary
}).reset_index()

rfm.head()

"""# Safe RFM Scoring"""

def safe_qcut(series, q, reverse=False):
    try:
        if reverse:
            labels = list(range(q, 0, -1))
        else:
            labels = list(range(1, q+1))
        return pd.qcut(series, q=q, labels=labels, duplicates='drop')
    except:
        return pd.cut(series, bins=q, labels=labels)

rfm['R_Score'] = safe_qcut(rfm['Recency'], 4, reverse=True)
rfm['F_Score'] = safe_qcut(rfm['Frequency'], 4)
rfm['M_Score'] = safe_qcut(rfm['Monetary'], 4)

print(rfm)

print(rfm.dtypes)

def segment(row):
    if row['RFM_Score'] == '444':
        return 'Champions'
    elif int(row['R_Score']) >= 3 and int(row['F_Score']) >= 3:
        return 'Loyal Customers'
    elif int(row['R_Score']) == 4:
        return 'Recent Customers'
    elif int(row['R_Score']) == 1:
        return 'Lost Customers'
    else:
        return 'Potential Customers'

rfm['Segment'] = rfm.apply(segment, axis=1)

rfm[['Customer_ID','RFM_Score','Segment']].head()

print(rfm['Segment'].value_counts())

rfm.sort_values(by='RFM_Score', ascending=False).head()

def strategy(segment):
    if segment == "Champions":
        return "Premium rewards"
    elif segment == "Loyal Customers":
        return "Loyalty offers"
    elif segment == "Lost Customers":
        return "Re-engagement campaign"
    else:
        return "Marketing campaigns"

def recommendation(segment):
    if segment == "Champions":
        return "Upgrade credit card"
    elif segment == "Loyal Customers":
        return "Cashback offers"
    elif segment == "Lost Customers":
        return "Discount offers"
    else:
        return "Welcome benefits"

rfm['Strategy'] = rfm['Segment'].apply(strategy)
rfm['Recommendation'] = rfm['Segment'].apply(recommendation)

from sklearn.preprocessing import StandardScaler
from sklearn.cluster import KMeans

features = rfm[['Recency','Frequency','Monetary']]

scaler = StandardScaler()
scaled = scaler.fit_transform(features)

kmeans = KMeans(n_clusters=4, random_state=42)
rfm['Cluster'] = kmeans.fit_predict(scaled)

from sklearn.decomposition import PCA

pca = PCA(n_components=2)
reduced = pca.fit_transform(scaled)

