plt.title("KMeans Clusters")
plt.show()

"""# Deep Learning (Autoencoder)"""

import tensorflow as tf
from tensorflow.keras import layers, models

input_dim = scaled.shape[1]

input_layer = layers.Input(shape=(input_dim,))
encoded = layers.Dense(16, activation='relu')(input_layer)
encoded = layers.Dense(8, activation='relu')(encoded)
bottleneck = layers.Dense(4, activation='relu')(encoded)

decoded = layers.Dense(8, activation='relu')(bottleneck)
decoded = layers.Dense(16, activation='relu')(decoded)
output_layer = layers.Dense(input_dim, activation='linear')(decoded)

autoencoder = models.Model(input_layer, output_layer)
autoencoder.compile(optimizer='adam', loss='mse')

autoencoder.fit(scaled, scaled, epochs=30, batch_size=32)

encoder = models.Model(input_layer, bottleneck)
encoded_data = encoder.predict(scaled)

kmeans_dl = KMeans(n_clusters=4, random_state=42)
rfm['DL_Cluster'] = kmeans_dl.fit_predict(encoded_data)

